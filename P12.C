//Write a program in C to find whether a given number n is odd or even




#include <stdio.h>

int main (void)


{
    int n;
    printf("Enter the value of n: ");
    scanf("%d", &n);

    if (n>0)
        printf("n is the even number ", &n);

    else if (n<0)
        printf("n is the odd number ", &n);










    return 0;
}
